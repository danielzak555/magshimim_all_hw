def func(num1, num2):
    """Function to return the sum of two numbers.
    param num1: first number
    type num1: int
    param num2: second number
    type num2: int
    return: sum of two numbers
    rtype: int
    """
    return num1 + num2

def main():
    print(func(2, 3))

if __name__ == "__main__":
    main()
    